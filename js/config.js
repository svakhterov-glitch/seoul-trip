/* ============================================================
   КОНФИГ ПРИЛОЖЕНИЯ
   Опционально подхватывает config.local.js из корня проекта
   (он в .gitignore, со своими ключами). Если файла нет —
   возвращает {}, и приложение работает локально (localStorage,
   mock-ИИ) ровно как раньше.
   ============================================================ */

let _cfg;

export async function getConfig() {
  if (_cfg) return _cfg;
  try {
    const m = await import("../config.local.js");
    _cfg = m.default || {};
  } catch {
    _cfg = {}; // файла нет — локальный режим
  }
  return _cfg;
}
