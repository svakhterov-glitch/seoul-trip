/** Цвет метки по типу места (ключ PLACE_KINDS) — для дифференциации внутри дня. */
const KIND_COLORS: Record<string, string> = {
  food: '#e8463c',      // кафе/ресторан — коралловый
  museum: '#2f6fd6',    // музей — синий
  nature: '#3fa34d',    // природа — зелёный
  sight: '#d98a1b',     // достопримечательность — янтарный
  shop: '#9a55c9',      // шопинг — фиолетовый
  fun: '#e36aa0',       // развлечения — розовый
  bar: '#0f8b8d',       // бар — тёмная бирюза
  hotel: '#6b7385',     // отель — серый
  transport: '#5a7d8c', // транспорт — сине-серый
  other: '#8a93a8',     // прочее — нейтральный
};

/** Цвет для типа места; неизвестный тип → нейтральный серо-синий. */
export function kindColor(kind: string): string {
  return KIND_COLORS[kind] || '#8a93a8';
}
